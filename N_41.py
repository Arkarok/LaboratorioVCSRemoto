print("Algoritmo que imprima el nombre de cada numero en el rango de (0,10)")

for i in range(0,11):
  if i==0:
    print(i, "Cero")
  elif i==1:
    print(i, "Uno")
  elif i==2:
    print(i, "Dos")
  elif i==3:
    print(i, "Tres")
  elif i==4:
    print(i, "Cuatro")
  elif i==5:
    print(i, "Cinco")
  elif i==6:
    print(i, "Seis")
  elif i==7:
    print(i, "Siete")
  elif i==8:
    print(i, "Ocho")
  elif i==9:
    print(i, "Nueve")
  else:
    print(i, "Diez")

print("Fin del programa")    

